const wchar_t *OuiVendors_Lookup(const unsigned char mac[6]) {
    const unsigned long prefix = ((unsigned long)mac[0] << 16) | ((unsigned long)mac[1] << 8) | mac[2];

    int lo = 0, hi = kOuiTableSize - 1;
    while (lo <= hi) {
        const int mid = (lo + hi) / 2;
        if (kOuiTable[mid].prefix == prefix) {
            return kOuiTable[mid].vendor;
        }
        if (kOuiTable[mid].prefix < prefix) {
            lo = mid + 1;
        } else {
            hi = mid - 1;
        }
    }
    return NULL;
}
